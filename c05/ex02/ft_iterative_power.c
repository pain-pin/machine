#include <unistd.h>
#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	ret;

	ret = 1;
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	while (power-- > 0)
		ret *= nb;
	return (ret);
}
