#include <unistd.h>
#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	fac;

	fac = 1;
	if (nb < 2)
		return (1);
	while (nb > 1)
		fac *= nb--;
	return (fac);
}
