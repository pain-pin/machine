#include <unistd.h>
#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (n--)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
}
